let k = 0;  // Variável para contar os números
let soma = 0;  // Variável para armazenar a soma

const indice = 5;  // Índice até o qual a soma será calculada

while (k < indice) {
  k = k + 1;  // Incrementa o valor de k em 1 a cada iteração
  soma = soma + k;  // Adiciona o valor de k à soma
}

console.log(soma);  // Exibe o resultado da soma no console

